// フォントデータは "OSASK version 4.7" の物をお借りしています。
// フォントの作成者は 平木敬太郎さん と 聖人（Kiyoto）さん です。
// 元ファイル（newstyle.txt）は KL-01ライセンス で公開されています。
//  http://osask.net/
//  https://osdn.jp/projects/osask/scm/svn/tree/211/OSAKA/tags/4.7.0/
//
//
// [1]　フォントの形状を 8x16 の2進数で表す（「0：.」「1：*」で表現）
//  ........
//  ...*....
//  ..***...
//  .**.**..
//  **...**.
//  **...**.
//  **...**.
//  *******.
//  **...**.
//  **...**.
//  **...**.
//  **...**.
//  ........
//  ........
//  ........
//  ........
//  
//  [2]　各行を16進数に変換
//  ........　→　00000000　→　0x00
//  ...*....　→　00010000　→　0x10
//  ..***...　→　00111000　→　0x38
//  .**.**..　→　01101100　→　0x6C
//  　　〜〜〜〜〜
//  ........　→　00000000　→　0x00
//  
//  [3]　マクロで配列変数に格納
//  SetDebugFontData( DebugFontData[xxx], 0x00, 0x10, 0x38, 0x6C,　〜〜〜〜〜　0x00 );
//
// OpenXOPS起動時には逆展開を行い、DirectXおよびOpenGLでテクスチャフォントを生成します。
//
//
// ※OpenXOPSにおいて、本フォントデータを編集・改変する前提はありません。

